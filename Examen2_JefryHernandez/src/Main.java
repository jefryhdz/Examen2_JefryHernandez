    public void cargar() {
        File f = new File("./usuarios.fac");
        Usuario us = null;
        try {
            if (f.exists()) {
                FileInputStream entrada = new FileInputStream(f);
                ObjectInputStream objeto = new ObjectInputStream(entrada);

                try {
                    while ((us = (Usuario) objeto.readObject()) != null) {
                        listausuarios.add(us);

                    }
                } catch (Exception e) {
                }
            }
        } catch (Exception e) {
        }
    }

    public void cargaralbum() {
        File f = new File("./albumes.fac");
        Album al = null;
        try {
            if (f.exists()) {
                FileInputStream entrada = new FileInputStream(f);
                ObjectInputStream objeto = new ObjectInputStream(entrada);

                try {
                    while ((al = (Album) objeto.readObject()) != null) {
                        listalbums.add(al);

                    }
                } catch (Exception e) {
                }
            }
        } catch (Exception e) {
        }
    }

    public void agregar() {
        File f = new File("./albumes.fac");
        FileOutputStream fw = null;
        ObjectOutputStream bw = null;
        try {
            fw = new FileOutputStream(f);
            bw = new ObjectOutputStream(fw);
            try {
                for (Album li : listalbums) {
                    bw.writeObject(li);
                }
                bw.flush();
                try {
                    bw.close();
                    fw.close();
                } catch (Exception e) {
                }
            } catch (Exception e) {
            }
        } catch (Exception e) {

        }

    }

